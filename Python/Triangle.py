class Triangle:
    def __init__(self, pointA, pointB, pointC):
        self.pointA = pointA
        self.pointB = pointB
        self.pointC = pointC

    def getPointA(self):
        return self.pointA

    def setPointA(self, pointA):
        self.pointA = pointA

    def getPointB(self):
        return self.pointB

    def setPointB(self, pointB):
        self.pointB = pointB

    def getPointC(self):
        return self.pointC

    def setPointC(self, pointC):
        self.pointC = pointC
